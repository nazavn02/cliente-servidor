    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String doc,nom,ape,dir,ciu,tipo,correo,sex; 
        
        doc = jTextField_documento.getText();
        tipo= jComboBox_tipo.getSelectedItem().toString();
        nom = jTextField_nombres.getText();
        ape = jTextField_apellidos.getText();
        dir = jTextField_direccion.getText();        
        ciu = jTextField_cod_ciudad.getText();
        correo = jTextField_correo.getText();
        sex = jComboBox_sexo.getSelectedItem().toString();
     
        control_cliente contr = new control_cliente(doc, tipo, nom, ape, dir,sex,correo,Integer.parseInt(jTextField_cod_ciudad.getText()));
        if(!doc.equals("") && !tipo.equals("") && !nom.equals("") && !ape.equals("") && !dir.equals("") && !ciu.equals("") )
        {
        if(contr.registrar("cliente"))
        {
        JOptionPane.showMessageDialog(null,"El cliente se registro con exito ","Mensaje",JOptionPane.INFORMATION_MESSAGE);
        limpiar();
       
        }
        else
        {
        JOptionPane.showMessageDialog(this, "Error al registrar el cliente");
        }
        }
        else
        {
          JOptionPane.showMessageDialog(this, "Hay campos obligatorios");  
        }
        
               // TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

